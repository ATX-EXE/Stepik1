# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def is_prime(num):
    return len([i for i in range(1, num + 1) if num % i == 0]) == 2


# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def get_next_prime(num):
    f = num + 1
    while not is_prime(f):
        f += 1
    return f


