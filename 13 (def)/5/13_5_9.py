def is_correct_bracket(text):
    # –û–±—ä—è–≤–ª—è–µ–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—É—é
    total = 0
    text = list(text)
    for i in range(len(text)):
        if text[i] == '(':
            total += 1
        if text[i] == ')':
            total -= 1
        if total < 0:
            return False
    if total == 0:
        return True
    else:
        return False

