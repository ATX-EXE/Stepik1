# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —á–∏—Å–µ–ª –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∏–∑ –Ω–∏—Ö.

a, b = int(input()), int(input())  # a < b
num, total = 0, 0
for i in range(a, b + 1):
    test_total = 0
    for j in range(1, i + 1):
        if i % j == 0:
            test_total += j
    if test_total >= total:
        total, num = test_total, i
print(num, total)
